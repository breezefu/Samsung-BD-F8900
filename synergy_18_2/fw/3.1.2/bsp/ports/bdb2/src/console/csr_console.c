static void putCharacter(CsrInt16 x, CsrInt16 y, CsrUint8 character, CsrUint32 foregroundColor, CsrUint32 backgroundColor)
{
    CsrUint8 *characterPointer;
    CsrInt16 row, column, bitmask;

    characterPointer = (CsrUint8 *) charSet8x16 + character * FONT_HEIGHT;

    for (row = 0; row < FONT_HEIGHT; row++)
    {
        bitmask = *characterPointer++;
        for (column = 0; column < FONT_WIDTH; column++, bitmask = (bitmask << 1))
        {
            if (bitmask & 0x80)
            {
                LCD_SET_PIXEL((CsrInt16) (x + column), (CsrInt16) (y + row), foregroundColor);
            }
            else
            {
                LCD_SET_PIXEL((CsrInt16) (x + column), (CsrInt16) (y + row), backgroundColor);
            }
        }
    }
}

/* Clear Screen */
void ConsoleClear(void)
{
    CsrUint32 x, y;

    for (y = 0; y < LCD_HEIGHT; y++)
    {
        for (x = 0; x < LCD_WIDTH; x++)
        {
            LCD_SET_PIXEL(x, y, LCD_RGB(0, 0, 0));
        }
    }
    currentCharPosX = 0;
    currentCharPosY = 0;
    wrapped = FALSE;
}

void ConsoleRouteSet(CsrUint8 route)
{
    if (!(consoleRoute & CONSOLE_ROUTE_UART) && (route & CONSOLE_ROUTE_UART))
    {
        /* Route UART_CHANNEL to Debug UART */
        CsrUint8 byte = 0xA0;
        I2CWrite(0, 0x22, 1, &byte);
        UARTInitialise(UART_CHANNEL, UART_BAUD_115200, UART_CONF_8N1, FALSE, RX_BUFFER_SIZE, rxBuffer, TX_BUFFER_SIZE, txBuffer);
    }
    if ((consoleRoute & CONSOLE_ROUTE_UART) && !(route & CONSOLE_ROUTE_UART))
    {
        UARTDeinitialise(UART_CHANNEL);
    }
    if (!(consoleRoute & CONSOLE_ROUTE_DISPLAY) && (route & CONSOLE_ROUTE_DISPLAY))
    {
        /* Initialise Display and reset character position */
        LCDInitialise(LCD_FB0, NULL);
        currentCharPosX = 0;
        currentCharPosY = 0;
        wrapped = FALSE;
    }

    consoleRoute = route;
}

#define SCROLL_LINES     5
static void lcdVerticalScroll(CsrUint32 lines)
{
    int i;
    CsrUint32 *ptr;

    ptr = (CsrUint32 *) LCD_FB0;

    for (i = 0; i < (LCD_HEIGHT * LCD_WIDTH) - (SCROLL_LINES * FONT_HEIGHT * LCD_WIDTH); i++)
    {
        *ptr = *(ptr + (SCROLL_LINES * FONT_HEIGHT * LCD_WIDTH));
        ptr++;
    }
    for (i = 0; i < (SCROLL_LINES * FONT_HEIGHT * LCD_WIDTH); i++)
    {
        *ptr++ = CONSOLE_BGCOLOUR;
    }

    currentCharPosY = LINES - SCROLL_LINES;
}

/* Retargeting */

struct __FILE
{
    int      handle;
    CsrUint8 lastChar;
    CsrBool  backspace;
};

FILE __stdout = {0, 0, FALSE};
FILE __stdin = {1, 0, FALSE};
FILE __stderr = {2, 0, FALSE};

int fgetc(FILE *f)
{
    CsrUint8 c;
    CsrUint32 written;

    if (!(consoleRoute & CONSOLE_ROUTE_UART))
    {
        return EOF;
    }

    if (f->backspace)
    {
        f->backspace = FALSE;
    }
    else
    {
        while (TRUE)
        {
            written = UARTReceive(UART_CHANNEL, 1, &c);
            if (written > 0)
            {
                f->lastChar = c;
                f->backspace = FALSE;
                break;
            }
            else
            {
                NU_Sleep(1);
            }
        }
    }

    return (int) f->lastChar;
}

int __backspace(FILE *f)
{
    f->backspace = TRUE;
    return 0;
}

int fputc(int ch, FILE *f)
{
    if (consoleRoute & CONSOLE_ROUTE_UART)
    {
        CsrUint8 byte = (CsrUint8) ch;
        if (byte == '\n')
        {
            byte = '\r';
            UARTTransmit(UART_CHANNEL, 1, &byte);
            byte = '\n';
        }
        UARTTransmit(UART_CHANNEL, 1, &byte);
    }
    if (consoleRoute & CONSOLE_ROUTE_DISPLAY)
    {
        if (((CsrUint8) ch) != '\n')
        {
            wrapped = FALSE;
            putCharacter(FONT_WIDTH * currentCharPosX, FONT_HEIGHT * currentCharPosY, (CsrUint8) (ch & 0xFF), textColour, bgColour);
            currentCharPosX++;
            if (currentCharPosX >= COLUMNS)
            {
                wrapped = TRUE;
                currentCharPosX = 0;
                if (currentCharPosY < LINES - 1)
                {
                    currentCharPosY++;
                }
                else
                {
                    lcdVerticalScroll(FONT_HEIGHT);
                }
            }
        }
        else if (!wrapped) /* '\n' */
        {
            wrapped = FALSE;
            currentCharPosX = 0;
            if (currentCharPosY < LINES - 1)
            {
                currentCharPosY++;
            }
            else
            {
                lcdVerticalScroll(FONT_HEIGHT);
            }
        }
        else
        {
            wrapped = FALSE;
        }
    }

    return ch;
}

int ferror(FILE *f)
{
    return EOF;
}

CsrBool ConsoleKbhit(void)
{
    if (consoleRoute & CONSOLE_ROUTE_UART)
    {
        if (__stdin.backspace)
        {
            return TRUE;
        }
        else
        {
            if (UARTReceiveDataCountGet(UART_CHANNEL))
            {
                return TRUE;
            }
        }
    }
    return FALSE;
}

